    /** Modules\\\Area\\\Database\\\Seeders\\\SeedCountriesTableSeeder
     * Run the database seeds.
     *
     * @return void
     */
    public function run()
    {
        $codes = (new Countries())->all()->pluck('cca2', 'currencies.0')->toArray();
        $names = (new Countries())->all()->pluck('translations', 'currencies.0')->toArray();

        $cities = [];

        foreach ($codes as $currency => $code) {
            if ($currency && isset($names[$currency]['ces']['common'])) {
                $country = Country::updateOrCreate([
                    'currencies_code' => $currency,
                    'title' => ['ar' => $names[$currency]['urd']['common'], 'en' => $names[$currency]['ces']['common']],
                    'slug' => ['ar' => $names[$currency]['urd']['common'], 'en' => $names[$currency]['ces']['common']],
                    'code' => $code,
                ], [

                    'title' => ['ar' => $names[$currency]['urd']['common'], 'en' => $names[$currency]['ces']['common']],
                    'slug' => ['ar' => $names[$currency]['urd']['common'], 'en' => $names[$currency]['ces']['common']],
                    'code' => $code,
                    'currencies_code' => $currency,
                ]);

                if($currency == 'KWD'){
                    $country->update([
                        'title' => ['ar' => 'الكويت', 'en' => $names[$currency]['ces']['common']],
                        'slug' => ['ar' => 'الكويت', 'en' => $names[$currency]['ces']['common']],
                    ]);
                    (new SeedCitiesTableSeeder())->run($country->id);
                }else{
                    $states_in_ar = (new Countries())->where('cca2',
                        $code)->first()->hydrateStates()->states->pluck('extra.name_ar', 'postal')->toArray();
                    $states_in_en = (new Countries())->where('cca2',
                        $code)->first()->hydrateStates()->states->pluck('extra.name_en', 'postal')->toArray();

                    foreach ($states_in_en as $postal => $state) {
                        if ($postal) {
                            $state_ar = isset($this->cities[$postal]) && $this->cities[$postal] ? $this->cities[$postal] : $states_in_ar[$postal];
                            $cities[$postal] = $state;

                            $country->cities()->updateOrCreate([
                                'postal' => $postal,
                                'title' => ['ar' => $state_ar, 'en' => $state],
                                'slug' => ['ar' => $state_ar, 'en' => $state],
                            ],[
                                'title' => ['ar' => $state_ar, 'en' => $state],
                                'slug' => ['ar' => $state_ar, 'en' => $state],
                            ]);
                        }
                    }
                }
            }
        }

    }
